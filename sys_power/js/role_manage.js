/**选中或取消权限
*/
function selectpowertype(obj){
	var arr = obj.id.split("_");
		pp_id = arr[1],
		pt_id = arr[2];
		
	if(obj.checked){
		da.runDB("/sys_power/action/power2role_add_item.php",{
			prid: g_prid,
			ppid: pp_id,
			ptid: pt_id
			
		},function(res){
			if("FALSE" == res){
				alert("设置失败");
			}else{
				alert("设置成功");
			}
		});
	}
	else{
		da.runDB("/sys_power/action/power2role_delete_item.php",{
			prid: g_prid,
			ppid: pp_id
			
		},function(res){
			if("FALSE" == res){
				alert("设置失败");
			}else{
				alert("设置成功");
			}
		});
	}
}


